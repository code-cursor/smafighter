  function renderDots() {
    return storyPages
      .map(
        (_, index) => `
          <button
            type="button"
            data-story-dot="${index}"
            aria-label="Go to story page ${index + 1}"
            class="h-2.5 rounded-full transition-all duration-300 ${
              index === currentPage
                ? "w-8 bg-[#e90686]"
                : "w-2.5 bg-pink-200 hover:bg-pink-400"
            }"
          ></button>
        `
      )
      .join("");
  }

  function renderStory() {
    const page = storyPages[currentPage];
    const progress = ((currentPage + 1) / storyPages.length) * 100;

    rootElement.innerHTML = `
      <div class="relative overflow-hidden rounded-[2rem] border border-pink-100 bg-white/80 p-4 md:p-6 shadow-2xl backdrop-blur">
        <div class="absolute left-0 top-0 h-1.5 bg-[#e90686] transition-all duration-500" style="width: ${progress}%"></div>

        <div
          data-story-card
          class="min-h-[560px] rounded-[1.5rem] bg-gradient-to-br from-white via-pink-50/70 to-white p-5 md:p-8 opacity-0 translate-y-3 transition-all duration-500"
        >
          ${renderPageContent(page)}
        </div>

        <div class="mt-6 flex flex-col gap-5 border-t border-pink-100 pt-5">
          <div class="flex flex-wrap items-center justify-center gap-2">
            ${renderDots()}
          </div>

          <div class="flex items-center justify-between gap-3">
            <button
              type="button"
              id="storyPrevBtn"
              class="rounded-full bg-[#eae5d9] px-5 py-2.5 text-sm md:text-base font-bold text-[#e90686] transition hover:bg-[#e90686] hover:text-white disabled:cursor-not-allowed disabled:opacity-40"
              ${currentPage === 0 ? "disabled" : ""}
            >
              ‹ Prev
            </button>

            <div class="rounded-full bg-pink-50 px-4 py-2 text-sm font-bold text-gray-600">
              Page ${currentPage + 1} of ${storyPages.length}
            </div>

            <button
              type="button"
              id="storyNextBtn"
              class="rounded-full bg-[#eae5d9] px-5 py-2.5 text-sm md:text-base font-bold text-[#e90686] transition hover:bg-[#e90686] hover:text-white disabled:cursor-not-allowed disabled:opacity-40"
              ${currentPage === storyPages.length - 1 ? "disabled" : ""}
            >
              Next ›
            </button>
          </div>
        </div>
      </div>
    `;

    const card = rootElement.querySelector("[data-story-card]");
    requestAnimationFrame(() => {
      card.classList.remove("opacity-0", "translate-y-3");
    });

    rootElement.querySelector("#storyPrevBtn").addEventListener("click", prevPage);
    rootElement.querySelector("#storyNextBtn").addEventListener("click", nextPage);

    rootElement.querySelectorAll("[data-story-dot]").forEach((dot) => {
      dot.addEventListener("click", function () {
        goToPage(Number(this.dataset.storyDot));
      });
    });
  }

  function nextPage() {
    if (currentPage < storyPages.length - 1) {
      currentPage += 1;
      renderStory();
    }
  }

  function prevPage() {
    if (currentPage > 0) {
      currentPage -= 1;
      renderStory();
    }
  }

  function goToPage(index) {
    if (index >= 0 && index < storyPages.length) {
      currentPage = index;
      renderStory();
    }
  }

  window.loadOliviaStory = function (rootId) {
    rootElement = document.getElementById(rootId);

    if (!rootElement) {
      console.warn("Olivia story root element not found.");
      return;
    }

    renderStory();

    document.addEventListener("keydown", function (event) {
      if (event.key === "ArrowRight") nextPage();
      if (event.key === "ArrowLeft") prevPage();
    });
  };
